function fit = fitness(present)
fit=3*(1-present(1)).^2.*exp(-(present(1).^2) - (present(2)+1).^2) ...                                          %**需要求极值的函数,本例即peaks函数
    - 10*(present(1)/5 - present(1).^3 - present(2).^5).*exp(-present(1).^2-present(2).^2) ...
    - 1/3*exp(-(present(1)+1).^2 - present(2).^2);
end